## Use Python range() function with 1 parameter to display: 0,1,2,3
for i in range(4):
    print(i)

## Use Python range() function with 3 parameters to display: 3,2,1,0
for i in range(3, -1 , -1):
    print(i)

## Use Python range() function with 3 parameters to display: 8,6,4,2
for i in range (8, 0, -2):
    print (i)
